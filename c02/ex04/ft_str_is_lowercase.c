int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	c;

	i = 0;
	c = 0;
	while (str[i])
	{
		if (str[i] >= 97 && str[i] <= 122)
		{
			++c;
		}
		++i;
	}
	if (c == i)
		return (1);
	else
		return (0);
}
