int	ft_str_is_printable(char *str)
{
	int	i;
	int	c;

	i = 0;
	c = 1;
	while (str[i])
	{
		if (str[i] < ' ' || str[i] > '~')
		{
			c = 0;
		}
		++i;
	}
	return (c);
}
